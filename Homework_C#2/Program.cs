// Задача 10: Напишите программу, которая принимает на вход трёхзначное число и на выходе показывает вторую цифру этого числа. 456 -> 5 //
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void task10()
{
    String number;
    while (true)
    {
        Console.Write("Введите трехзначное число: ");
        number = Console.ReadLine();
        if (number.Length == 3) break;
        Console.Write("Число не трехзначное! ");
    }
    printSecondDigit(number);
}

void printSecondDigit(String number)
{
    Console.WriteLine(number[1]);
}


