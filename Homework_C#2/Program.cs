//Задача 15: Напишите программу, которая принимает на вход цифру, обозначающую день недели, и проверяет, является ли этот день выходным. 6 -> да //
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
void task15()
{
    int number;
    while (true)
    {
        Console.WriteLine("Чтобы выйти введите число 0.");
        Console.Write("Введите число обозначающую день недели: ");
        number = int.Parse(Console.ReadLine());
        if (number == 0) break;
        if (number > 0 && number <= 7)
        {
            if (number == 6 || number == 7)
            {
                Console.WriteLine(number + " -> да");
            }
            else
            {
                Console.WriteLine(number + " -> нет");
            }
        }
        else
        {
            Console.WriteLine("Цифра не обозначает день недели!\n");
        }
    }
}